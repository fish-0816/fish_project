# ╔══════════════════════════════════════════════════════════╗
# ║              ALIEN INVASION  —  Quick Reference          ║
# ╠══════════════════════════════════════════════════════════╣
# ║  MOUSE   Click "Play"           Start / restart game     ║
# ║  ← →     Arrow keys            Move ship left / right    ║
# ║  ↑ ↓     Arrow keys            Move ship up / down       ║
# ║  SPACE   Fire bullet(s)        Normal / rapid / multi    ║
# ║  P       Pause / resume        Freezes all simulation    ║
# ║  Q       Quit                  Exit immediately          ║
# ╠══════════════════════════════════════════════════════════╣
# ║  OBJECTIVES                                              ║
# ║  • Destroy all aliens in a wave to advance to the next   ║
# ║  • Each wave moves faster and scores more points         ║
# ║  • Avoid alien collisions and aliens reaching the floor  ║
# ║  • You have 3 lives — losing all ends the game           ║
# ╠══════════════════════════════════════════════════════════╣
# ║  POWER-UPS  (drop randomly from destroyed aliens)        ║
# ║   RAPID   Faster, more bullets at once (~6 s)            ║
# ║   SHIELD  Absorbs the very next hit (one use)            ║
# ║   MULTI   Fires three spread bullets at once (~6 s)      ║
# ╠══════════════════════════════════════════════════════════╣
# ║  COMBO SYSTEM                                            ║
# ║  Chain kills within ~1.3 s to build a multiplier         ║
# ║  up to 5× — letting the chain expire resets it to 1×     ║
# ╚══════════════════════════════════════════════════════════╝